export function getQuoteFromNumbers(num1: number, num2: number, num3: number): string {
  const sum = num1 + num2 + num3;
  const product = (num1 + 1) * (num2 + 1) * (num3 + 1);
  const index = (sum + product) % quotes.length;
  
  return quotes[index];
}
